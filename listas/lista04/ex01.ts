// 01 · Primeiro e último
// Dado um array de nomes, mostre o primeiro e o último. O último índice é length - 1.

let listaNomes = ["Helena", " Gabriele", " Felipe", " Oscar", " Mariana"];
let primeiroNomeLista = listaNomes [0];
let ultimoNomeLista = listaNomes [listaNomes.length - 1];

alert (`Nomes listados: ${listaNomes}
Primeiro nome: ${primeiroNomeLista}
Ultimo nome: ${ultimoNomeLista}`);